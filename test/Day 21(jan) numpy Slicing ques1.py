
#1. Write a NumPy program to create an array of 
# 10 zeros, 10 ones, and 10 fives

import numpy as np

# Array of 10 zeros
zeros = np.zeros(10)
print("Zeros Array:", zeros)

# Array of 10 ones
ones = np.ones(10)
print("Ones Array:", ones)

# Array of 10 fives
fives = np.ones(10) * 5
print("Fives Array:", fives)
